    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        rbDisable();
        int selectedRow = wrkReqTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to check the status.");
            return;
        } else {
            String message = null;
            WasteWorkRequest hosprequest = (WasteWorkRequest) wrkReqTable.getValueAt(selectedRow, 0);

            message = hosprequest.getStatus();

            if (message != null) {
                if (message.equals("Sent")) {
                    rbEnable();
                    rbOne.setSelected(true);
                    img1.setVisible(true);
                    statusJtext.setText(message);
                } else if (message.equals("Pending")) {
                    rbEnable();
                    rbTwo.setSelected(true);
                    img2.setVisible(true);
                    statusJtext.setText(message);
                } else if (message.equals("Sent to Recycle Admin")) {
                    rbEnable();
                    rbThree.setSelected(true);
                    img3.setVisible(true);
                    statusJtext.setText(message);
                } else if (message.equals("Pending For Recycling")) {
                    rbEnable();
                    rbFour.setSelected(true);
                    img4.setVisible(true);
                    statusJtext.setText(message);
                } else if (message.equals("Sent to Recycle Worker")) {
                    rbEnable();
                    rbFive.setSelected(true);
                    img5.setVisible(true);
                    statusJtext.setText(message);
                } else if (message.equals("Assigned")) {
                    rbEnable();
                    rbSix.setSelected(true);
                    img6.setVisible(true);
                    statusJtext.setText(message);
                } else {
                    rbEnable();
                    rbSeven.setSelected(true);
                    img7.setVisible(true);
                    statusJtext.setText(message);
                }
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

