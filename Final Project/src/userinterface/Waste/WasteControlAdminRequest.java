    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // TODO add your handling code here:
        HSSFWorkbook fWorkbook = new HSSFWorkbook();
        HSSFSheet fSheet = fWorkbook.createSheet("new Sheet");
        TreeMap<String, Object[]> data = new TreeMap<>();

        HSSFFont sheetTitleFont = fWorkbook.createFont();
        File file = new File(new File("").getAbsolutePath() + "/Waste.xls");
        HSSFCellStyle cellStyle = fWorkbook.createCellStyle();

        sheetTitleFont.setBoldweight(HSSFFont.BOLDWEIGHT_BOLD);
        //sheetTitleFont.setColor();
        TableModel model = wrkReqTable.getModel();
        TableColumnModel tcm = wrkReqTable.getColumnModel();
        HSSFRow fRow1 = fSheet.createRow((short) 0);

        for (int i = 0; i < model.getRowCount(); i++) {

            HSSFRow fRow = fSheet.createRow((short) i);
            for (int j = 0; j < model.getColumnCount(); j++) {
                HSSFCell cell = fRow.createCell((short) j);
                cell.setCellValue(tcm.getColumn(j).getHeaderValue().toString());
                cell.setCellValue(model.getValueAt(i, j).toString());
                cell.setCellStyle(cellStyle);

            }

        }
        try {
            FileOutputStream fileOutputStream;
            fileOutputStream = new FileOutputStream(file);
            BufferedOutputStream bos = new BufferedOutputStream(fileOutputStream);
            fWorkbook.write(bos);
            bos.close();
            fileOutputStream.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "vvxcvxcv");
        }
    }//GEN-LAST:event_btnExportActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) wrkReqTable.getModel();
        int selectedRow = wrkReqTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to assign the request",
                    "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            String statusFlag = (String) model.getValueAt(selectedRow, 3);
            if (statusFlag.equals("Completed") || statusFlag.equals("Sent to Recycle Admin")) {
                JOptionPane.showMessageDialog(null, "The work request cant be assigned as it is completed or sent to Recycle Admin");
                return;
            }

            WasteWorkRequest request = (WasteWorkRequest) wrkReqTable.getValueAt(selectedRow, 0);
            request.setReceiver(userAccount);
            request.setStatus("Pending");
            populateWorkRequestTable();
            btnSubmitResult.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Request assigned to you successfully",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    public void populateWorkRequestTable() {
        DefaultTableModel model = (DefaultTableModel) wrkReqTable.getModel();
        model.setRowCount(0);

        for (WorkRequest request : wasteControlAdminOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? "To be Assigned" : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();
            row[4] = ((WasteWorkRequest) request).getEmerLevel();
            row[5] = ((WasteWorkRequest) request).getTotalWaste();
            wrkReqTable.getColumnModel().getColumn(3).setMinWidth(0);
            wrkReqTable.getColumnModel().getColumn(3).setMaxWidth(0);
            wrkReqTable.getColumnModel().getColumn(3).setWidth(0);

            model.addRow(row);
        }

    }
