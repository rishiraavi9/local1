    private void btnSubmitResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitResultActionPerformed

        DefaultTableModel model = (DefaultTableModel) wrkReqTable.getModel();

        int selectedRow = wrkReqTable.getSelectedRow();

        if (selectedRow >= 0) {

            String assignedReceiver = (String) model.getValueAt(selectedRow, 2);

            String statusFlag = (String) model.getValueAt(selectedRow, 3);

            if (assignedReceiver == null) {
                JOptionPane.showMessageDialog(null, "Please assign a receiver",
                        "Warning", JOptionPane.WARNING_MESSAGE);
                return;

            } else if (statusFlag.equals("Sent")) {
                JOptionPane.showMessageDialog(null, "The work request cant be completed as it is not assigned yet");
                return;
            } else if (statusFlag.equals("Sent to Recycle Admin") || statusFlag.equals("Completed")) {
                JOptionPane.showMessageDialog(null, "The work request cant be pushed as it is sent to Recycle Admin/Completed");
                return;
            } else if (statusFlag.equals("Sent to Recycle Worker")) {
                JOptionPane.showMessageDialog(null, "The work request cant be pushed as it is sent to Recycle Worker");
                return;
            } else if (txtResult.getText() == null || txtResult.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Action to be taken cannot be blank");
                return;
            } else {

                WasteWorkRequest request = (WasteWorkRequest) (WorkRequest) wrkReqTable.getValueAt(selectedRow, 0);

                txtZipCode.setText("0" + String.valueOf(((WasteWorkRequest) request).getZipCode()));
                request.setResult(txtResult.getText());
                request.setStatus("Sent");
                request.setSentDate(timeStamp);

                populateWorkRequestTable();

                //push data to Recycle Admin
                WasteWorkRequest wt = (WasteWorkRequest) model.getValueAt(selectedRow, 0);

                RecycleControlAdmin r;
                String message = wt.getMessage();
                for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                        Role role = ua.getRole();
                        if (role instanceof RecycleControlAdmin) {
                            account = ua;
                        }
                    }
                }

                txtSentDate.setText(timeStamp.toString());
                txtAssignedRA.setText(account.toString());

                WasteWorkRequest request1 = new WasteWorkRequest();
                request1.setMessage(message);
                request1.setSender(userAccount);
                request1.setStatus("Sent to Recycle Admin");
                request1.setReceiver(account);
                request1.setEmerLevel(wt.getEmerLevel());
                request1.setTotalWaste(wt.getTotalWaste());
                request1.setZipCode(wt.getZipCode());
                request1.setResult(txtResult.getText());
                request1.setSentDate(timeStamp);

                Organization org = null;
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof RecycleAdminOrganization) {
                        org = organization;
                        break;
                    }
                }
                if (org != null) {
                    org.getWorkQueue().getWorkRequestList().add(request);
                    userAccount.getWorkQueue().getWorkRequestList().add(request);

                }

                // pushDataToRecycleAdmin();
                JOptionPane.showMessageDialog(null, "Request completed successfully",
                        "Information", JOptionPane.INFORMATION_MESSAGE);

            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to submit the request",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSubmitResultActionPerformed

